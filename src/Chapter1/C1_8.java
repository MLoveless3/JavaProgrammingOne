package Chapter1;

/**
 * Program to display the area and perimeter of a circle that has a radius of
 * 5.5 to the console
 *
 * @author Mickey Loveless II
 */
public class C1_8 {

    /**
     * Main Method
     *
     * @param args arguments from command line prompt
     */
    public static void main(String[] args) {
        System.out.println(5.5 * 2 * Math.PI);
        System.out.println(Math.pow(5.5, 2) * Math.PI);
    }
}
